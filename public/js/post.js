// Scroll Down
function scrollDownPosts(y) {
    var height = y;
    var a = window.scrollTo({
        top: height,
        behavior: "smooth"
    });
}

// Scroll Up
function scrollUpPosts(y) {
    var height = y;
    var a = window.scrollTo({
        top: height,
        behavior: "smooth"
    });
}